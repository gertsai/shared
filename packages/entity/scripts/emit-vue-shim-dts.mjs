// Sprint 3.11 CI fix — emit hand-written DTS for the `vue.ts` shim.
//
// `tsup` builds dist/vue.js + dist/vue.cjs from src/vue.ts (which is just
// `export { vueReactiveAdapter } from '@gertsai/entity-vue'`), but we
// disable DTS for that entry in tsup.config.ts because the DTS pipeline
// would try to resolve `@gertsai/entity-vue/dist/index.d.ts` at build
// time — and on a clean checkout entity-vue hasn't been built yet
// (entity-vue peer-deps entity → pnpm builds entity FIRST). See
// tsup.config.ts header for the full circular-peer-dep narrative.
//
// The hand-written DTS forwards types via `export *`. Module resolution
// happens at the CONSUMER's typecheck time, when entity-vue IS installed
// in their node_modules graph (peer dep enforced by pnpm/npm).
//
// Generates dist/vue.d.ts (ESM) + dist/vue.d.cts (CJS) — exact same
// content per ADR-008 dual-format publishing convention.
import { writeFileSync, mkdirSync, existsSync } from 'node:fs';
import { dirname, resolve } from 'node:path';
import { fileURLToPath } from 'node:url';

const here = dirname(fileURLToPath(import.meta.url));
const distDir = resolve(here, '..', 'dist');

if (!existsSync(distDir)) {
  mkdirSync(distDir, { recursive: true });
}

const banner = `// SPDX-License-Identifier: Apache-2.0
// Auto-generated by scripts/emit-vue-shim-dts.mjs — do not edit by hand.
// Re-export shim per ADR-008 Decision B + I-3. See tsup.config.ts header.
`;

const body = `${banner}\nexport { vueReactiveAdapter } from '@gertsai/entity-vue';\n`;

writeFileSync(resolve(distDir, 'vue.d.ts'), body);
writeFileSync(resolve(distDir, 'vue.d.cts'), body);

console.log('[emit-vue-shim-dts] wrote dist/vue.d.ts + dist/vue.d.cts');
